package com.xworkz.methods.string;

public class MethodE {

	public static void main(String[] args) {
		
		//43 valueOf boolean
		String new1=String.valueOf(true);
		System.out.println(new1);
		
		//44 valueOf double
		String new2=String.valueOf(25.00);
		System.out.println(new2);
		
		//45 valueOf char
		String new3=String.valueOf('a');
		System.out.println(new3);
		
		//46 valueOf int
		String new4=String.valueOf(55);
		System.out.println(new4);
		
		//47 valueOf float
		String new5=String.valueOf(75.5f);
		System.out.println(new5);
				
		//48 valueOf long
		String new6=String.valueOf(123456678910l);
		System.out.println(new6);
		
		//49 valueOf object
		String new7=String.valueOf(34.45);
		System.out.println(new7);
	}

}
